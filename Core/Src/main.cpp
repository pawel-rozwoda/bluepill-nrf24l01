void toggle_led()
{
	HAL_GPIO_WritePin(LED_PORT, LED_PIN, GPIO_PIN_RESET);
	HAL_Delay(100);
	HAL_GPIO_WritePin(LED_PORT, LED_PIN, GPIO_PIN_SET);
}

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_TIM1_Init();

  NRF24_Init();


#ifdef RECEIVE
  NRF24_RxMode(RxAddress, 10);
#endif

#ifdef TRANSMIT
  dht_obj.init(htim1, DHT22_PORT, DHT22_PIN);
  NRF24_TxMode(TxAddress, 10);
#endif


  NRF24_ReadAll(config_data);

  HAL_TIM_Base_Start(&htim1);

  HAL_Delay(100);

  while (1)
  {

#ifdef RECEIVE
	  if (isDataAvailable(2) == 1)
	  {
		  NRF24_Receive(RxData);
		  toggle_led();
	  }
#endif


#ifdef TRANSMIT
	  dht_obj.get_values(humidity, temperature);
	  if (NRF24_Transmit(TxData) == 1)
	  {
		  toggle_led();
	  }
	  HAL_Delay(1000);
#endif

  }
}

